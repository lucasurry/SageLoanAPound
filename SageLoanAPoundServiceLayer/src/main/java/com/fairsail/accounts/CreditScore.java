package com.fairsail.accounts;

public class CreditScore {
	private int score;
	private String source;
	
	public CreditScore() {}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	};
}
